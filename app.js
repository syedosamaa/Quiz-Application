  //? User Details get and set //

  
 document.getElementById('para-name').innerText = `${inpFName.value} ${inpLName.value}`;
 document.getElementById('para-email').innerText = inpEmail.value;
 document.getElementById('para-roll-no').innerText = inpRoll.value;
